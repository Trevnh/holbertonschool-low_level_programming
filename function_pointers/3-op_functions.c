#include "3-calc.h"

/**
 * op_add - return result of a + b
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return result of a - b
 * @a: first integer
 * @b: second integer
 *
 * Return: result of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return result of a multiplied by b
 * @a: first integer
 * @b: second integer
 *
 * Return: result of a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return result of a divided by b
 * @a: first integer
 * @b: second integer
 *
 * Return: result of a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return result of a modulo b
 * @a: first integer
 * @b: second integer
 *
 * Return: result of a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
